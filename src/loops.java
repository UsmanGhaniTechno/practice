public class loops {
    // Loops practice here //



    public static void main(String[] args)
    {
        int i = 0;

        while(i < 5)
        {
            System.out.println("while loop");
            i++;
        }

        for(int j = 0; j < 5; j++)
        {
            System.out.println("for loop");
        }

        String[] array = {"a", "b", "c", "d"};

        for (String k: array)
        {
            System.out.println(k);
        }









    }
}
